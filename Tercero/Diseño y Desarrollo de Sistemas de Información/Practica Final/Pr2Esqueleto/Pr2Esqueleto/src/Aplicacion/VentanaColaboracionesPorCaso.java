    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
        // TODO add your handling code here:
        manejaColabora mc = new manejaColabora(co);
        try {
            ResultSet rs = mc.listaColaboradoresPorCaso(jTextIntroduceCodigoCaso.getText());
            while (modeloColaboracionPorCaso.getRowCount() > 0) {//te da el numero de filas que hay en la tabla
                modeloColaboracionPorCaso.removeRow(0); //Elimina la fila siempre que haya alguna
            }
            while(rs.next() == true){
                String[] fila = new String[4];
                fila[0] = rs.getNString("CODEXPERTO");
                fila[1] = rs.getNString("NOMBRE_EXPERTO");
                fila[2] = rs.getNString("ESPECIALIDAD");
                fila[3] = rs.getNString("DESCRIPCION_COLABORACION");
                modeloColaboracionPorCaso.addRow(fila);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al obtener la lista de colaboraciones por caso.");
        }
    }//GEN-LAST:event_jButtonMostrarActionPerformed

