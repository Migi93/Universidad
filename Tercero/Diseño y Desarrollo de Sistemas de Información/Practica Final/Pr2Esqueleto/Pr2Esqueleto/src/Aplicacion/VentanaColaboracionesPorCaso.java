    public void ordenarcolaexpertos(){
    try {
            // TODO add your handling code here:
            
            ArrayList<experto> e = mexperto.listaExpertosordenadas();
            while (modeloColaboracionPorCaso.getRowCount() > 0) {//te da el numero de filas
                modeloColaboracionPorCaso.removeRow(0); //Elimina la fila
            }
            for (int i = 0; i < e.size(); i++) {
                String[] fila = {e.get(i).getCodExperto(), e.get(i).getNombre(), e.get(i).getEspecialidad(), e.get(i).getSexo()};
                
                modeloColaboracionPorCaso.addRow(fila); //Agrega una fila
            }
        } catch (SQLException ex1) {
            JOptionPane.showMessageDialog(null, "Error al ordenar toda la lista.");
        }
    }
    
